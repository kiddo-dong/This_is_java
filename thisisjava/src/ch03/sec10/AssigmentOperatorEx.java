package ch03.sec10;

public class AssigmentOperatorEx {
	public static void main(String[] args) {
		int result = 0;
		result += 10; // 10
		System.out.println(result);
		
		result -= 5; // 5
		System.out.println(result);
		
		result *= 3; // 15
		System.out.println(result);

		result /= 5; // 3
		System.out.println(result);
		
		result %= 3; //0
		System.out.println(result);
	}
}
